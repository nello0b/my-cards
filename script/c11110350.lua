If a monster you control attacks a Defense Position monster, inflict piercing battle damage to your opponent.
You can only use each of the following effects of "Dedicated Gaia The Fierce Knight" once per turn.
During the Main Phase (Quick Effect): You can Special Summon this card from your hand, also you can only Special Summon from the Extra Deck once for the rest of this turn.
If this card is Normal or Special Summoned: You can send up to 2 cards that mention "Light and Darkness Ritual" from your Deck to the GY, except "Dedicated Gaia The Fierce Knight".
